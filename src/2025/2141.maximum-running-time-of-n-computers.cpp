#include <vector>

// @lc code=start
class Solution {
 public:
  long long maxRunTime(const int n, const std::vector<int>& batteries) {
    const long long total_power_sum =
        std::accumulate(batteries.cbegin(), batteries.cend(), 0ll);
    long long left = 1;
    long long right = total_power_sum / n + 1;

    while (left < right) {
      const long long mid = (right - left) / 2 + left;
      const long long usable_power_sum = std::accumulate(
          batteries.cbegin(), batteries.cend(), 0ll,
          [&](const long long current_sum, const int battery) {
            return current_sum + std::min(static_cast<long long>(battery), mid);
          });
      if (usable_power_sum >= mid * n) {
        left = mid + 1;
      } else {
        right = mid;
      }
    }

    return left - 1;
  }
};
