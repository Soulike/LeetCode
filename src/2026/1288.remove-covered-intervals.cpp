#include <algorithm>
#include <vector>

// @lc code=start
class Solution {
 public:
  int removeCoveredIntervals(std::vector<std::vector<int>>& intervals) {
    std::ranges::sort(intervals, [](const std::vector<int>& interval1,
                                    const std::vector<int>& interval2) {
      if (interval1[0] != interval2[0]) {
        // Increasing begin time
        return interval1[0] < interval2[0];
      }
      // Decreasing end time
      return interval1[1] > interval2[1];
    });

    int remaining_interval_count = 1;
    int max_end = intervals[0][1];
    for (int i = 1; i < intervals.size(); i++) {
      if (intervals[i][1] > max_end) {
        remaining_interval_count++;
        max_end = intervals[i][1];
      }
    }

    return remaining_interval_count;
  }
};
